// Case - 1
// Named Function
function helloWorld() {
  // logic comes here
  console.log("hello world");
}

// Case - 2
// Annonymous Function
let helloWorld = function () {
  // logic comes here
  console.log("hello world");
};

// Case - 3
// Arrow Function
let helloWorld = () => {
  // logic comes here
  console.log("hello world");
};
